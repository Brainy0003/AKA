using System;

class NumericRepresentation
{
	static void Main()
	{
		uint DayOfBirth;
		uint MonthOfBirth;
		uint YearOfBirth;
	
		DayOfBirth   = 8;
		MonthOfBirth = 11;
		YearOfBirth  = 1996;

		Console.WriteLine("Red Oak High School");
		Console.Write("Student Date of Birth: ");
		Console.Write(MonthOfBirth);
		Console.Write("/");
		Console.Write(DayOfBirth);
		Console.Write("/");
		Console.Write(YearOfBirth);
		Console.WriteLine();
	}
}
